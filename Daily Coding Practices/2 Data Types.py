#Integer
nums1=10
print(nums1,type(nums1))

#float
nums2=10.5
print(nums2,type(nums2))

#Complex
nums3=10j
print(nums3,type(nums3))


#String
demostr="Hello World"
print(demostr,type(demostr))

#Tuple
students=["Ali","kashif","Noman","wajid"]
print(students,type(students))
print(students[0])

#Dictionary
employees={"name":"kashif",
           "age":32,
           "salary":8000,
           "scale":17  }
print(employees,type(employees))
print(employees['salary'])

#set
employeeData={"kashif",32,8000,17}
print(employeeData,type(employeeData))